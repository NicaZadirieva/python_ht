"""
Определение классов:
Создаем классы для пользователей, продуктов и заказов. У каждого класса есть атрибут ID и специфические поля, такие как email у пользователя или title у продукта.
Создание функции поиска get_by_id:
Функция принимает список элементов и искомый ID.
Возвращает элемент из списка с соответствующим ID или None, если ничего не найдено.
Использование обобщенного программирования (Generics) и протоколов:
Используем протокол Identifiable, который требует наличие ID у класса.
Создаем обобщенный тип T с ограничением (bound), что он должен быть Identifiable.
Импорт и применение:
Импортируем необходимые модули, такие как Optional и TypeVar.
Определяем функцию get_by_id с использованием типов и ограничений.
Функция проверяет наличие ID и возвращает соответствующий элемент.
Проверка работы функции:
Функцию можно использовать для поиска пользователя по ID в списке.
Если объект не соответствует протоколу Identifiable, выдаётся ошибка.
"""
from typing import Optional, Protocol, TypeVar

class Identifiable(Protocol):
    id: int

T = TypeVar("T", bound=Identifiable)

def get_by_id(arr: list[T], id: int) -> Optional[T]:
    for item in arr:
        if item.id == id:
            return item
    return None